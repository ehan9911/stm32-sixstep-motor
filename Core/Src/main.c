unsigned short PwmModeCheck(TIM_HandleTypeDef *htim)
{
	unsigned short oc1Mode, oc2Mode, oc3Mode;
	
	oc1Mode = (htim->Instance->CCMR1 & TIM_CCMR1_OC1M);
	oc2Mode = (htim->Instance->CCMR1 & TIM_CCMR1_OC2M) >> 8;
	oc3Mode = htim->Instance->CCMR2 & TIM_CCMR2_OC3M;
	if ((oc1Mode != oc2Mode) || (oc1Mode != oc3Mode)) {
		printf("TIMx PWM Mode not equal\r\n");
		return -1;
	}
	return oc1Mode;
}

